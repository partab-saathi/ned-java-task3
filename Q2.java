/*Q2 : Write a program to calculate the volume of a cylinder using the formula:
Volume=π×r2×h. */
public class Q2 {

    public static void main(String[] args) {
        double Pi;
        Pi = 3.14;
        double Radius;
        Radius = 2;
        int h;
        h = 20;
        double Volume = Pi * Radius * h;
        System.out.println("Volume = " + Volume);
    }
}
